package gui;

/**
 * The enum Password code.
 */
public enum PasswordCode {

    /**
     * Valid password password code.
     */
    VALID_PASSWORD,
    /**
     * Too short password code.
     */
    TOO_SHORT,
    /**
     * Too long password code.
     */
    TOO_LONG,
    /**
     * Must contain lowercase password code.
     */
    MUST_CONTAIN_LOWERCASE,
    /**
     * Must contain uppercase password code.
     */
    MUST_CONTAIN_UPPERCASE,
    /**
     * Must contain digit password code.
     */
    MUST_CONTAIN_DIGIT,
    /**
     * Must contain special password code.
     */
    MUST_CONTAIN_SPECIAL,
    /**
     * Character not allowed password code.
     */
    CHARACTER_NOT_ALLOWED
}
