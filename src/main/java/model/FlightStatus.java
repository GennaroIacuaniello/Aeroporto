package model;

/**
 * The enum Flight status.
 */
public enum FlightStatus {

    /**
     * Programmed flight status.
     */
    PROGRAMMED,
    /**
     * Cancelled flight status.
     */
    CANCELLED,
    /**
     * Delayed flight status.
     */
    DELAYED,
    /**
     * About to depart flight status.
     */
    ABOUT_TO_DEPART,
    /**
     * Departed flight status.
     */
    DEPARTED,
    /**
     * About to arrive flight status.
     */
    ABOUT_TO_ARRIVE,
    /**
     * Landed flight status.
     */
    LANDED

}
